package lec05;

public class Relational {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;

        System.out.println("a == b: " + (a == b)); // false
        System.out.println("a != b: " + (a != b)); // true

        System.out.println("a > b: " + (a > b)); // true
        System.out.println("a < b: " + (a < b)); // false
        System.out.println("a >= 10: " + (a >= 10)); // true
        System.out.println("b <= 5: " + (b <=5)); // false
    }
}
